/****************************************************************************************************
函数名：Delay
功能：延时毫秒
参数：mill：要延时的毫秒值
返回值：无
****************************************************************************************************/
void Delay(unsigned char mill)		//@11.894MHz
{
	unsigned char i;
	unsigned char j;

	while (mill--)
	{
		j=10;
		while (j--)
		{
			i=47;
			while (--i);
		}
	}
}
